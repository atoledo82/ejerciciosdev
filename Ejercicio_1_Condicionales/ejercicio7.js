let dia = prompt("Dime un dia de la semana").toLowerCase();

if (dia === "lunes"){
    console.log("Que tengas un excelente inicio de semana");
}else if (dia === "martes"){
    console.log("Que tengas un excelente Martes");
}else if (dia === "miercoles"){
    console.log("Que tengas un excelente Ombligo de semana");
}else if (dia === "jueves"){
    console.log("Que tengas un excelente Juebebes");
}else if (dia === "viernes"){
    console.log("Ya es fin de semana!");
}else if (dia === "sabado"){
    console.log("Ya es fin de semana que tengas un excelente sábado!");
}else if (dia === "domingo"){
    console.log("Ya es fin de semana y además es Domingo!");
}else{
    console.log("NO ENTIENDO NADA");
}